package org.nsu.dcis.core.core.exception;

public class AspectCloneException extends AmvException {

    public AspectCloneException() {
        super();
    }

    public AspectCloneException(String message) {
        super(message);
    }

    public AspectCloneException(String message, Throwable cause) {
        super(message, cause);
    }

    public AspectCloneException(Throwable cause) {
        super(cause);
    }
}
